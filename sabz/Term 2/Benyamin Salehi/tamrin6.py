from turtle import *

lt(90)
fillcolor('blue')
begin_fill()
circle(100, 180)
circle(200, 180)
lt(180)
circle(-100, 180)
end_fill()
penup()
goto(-60, 0)
pendown()
fillcolor('orange')
begin_fill()
circle(40)
end_fill()
penup()
goto(0, 0)
pendown()
begin_fill()
circle(100, 180)
lt(180)
circle(-200, 180)
circle(-100, 180)
end_fill()
penup()
goto(60, 0)
pendown()
fillcolor('blue')
begin_fill()
circle(-40)
end_fill()

done()